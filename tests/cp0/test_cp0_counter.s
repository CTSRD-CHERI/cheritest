.set mips64
.set noreorder
.set nobopt
.set noat

#
# Test that the rdhwr instruction can be used to read the high resolution
# cycle counter.
#

		.global test
test:		.ent test
		daddu 	$sp, $sp, -32
		sd	$ra, 24($sp)
		sd	$fp, 16($sp)
		daddu	$fp, $sp, 32

		# The counter is readable as hardware register 2
		# and CP0 register 9, select 0.
		# Hardware register 2 can be accessed if CP0 is accessible
		# (which is the case here) or if bit 2 in CP0.HWREna is set.

		li	$a1, 0
		lui	$a2, 0xffff

		mfc0	$a1, $9

		# The rdhwr instruction is from MIP32r2, so this test is not
		# expected to work on earlier MIPS revisions.

		.set push
		.set mips32r2
		rdhwr	$a2, $2
		.set pop

		ld	$fp, 16($sp)
		ld	$ra, 24($sp)
		daddu	$sp, $sp, 32
		jr	$ra
		nop			# branch-delay slot
		.end	test
