.set mips64
.set noreorder
.set nobopt
.set noat

#
# Test that if we set non-default values in a capability register, cmove to
# another register, that we can read the same non-default values back.
#

		.global test
test:		.ent test
		daddu 	$sp, $sp, -32
		sd	$ra, 24($sp)
		sd	$fp, 16($sp)
		daddu	$fp, $sp, 32

		# Put a non-zero value in c2.base, so we can tell when
		# c2 has been changed.
		dli		$t0, 0x100
		cincbase	$c1, $c0, $t0

		ccleartag $c1

		# Move should copy c1's tag and it current value (base=0)
		# into c2.
		cmove	$c2, $c1

		cgettag $a0, $c2 	# Should be 0
		cgetbase $a1, $c2     	# Should be 0x100

		ld	$fp, 16($sp)
		ld	$ra, 24($sp)
		daddu	$sp, $sp, 32
		jr	$ra
		nop			# branch-delay slot
		.end	test
