#
# Test that ctoptr of a capability with the tag bit unset returns 0
#

.include "macros.s"
.set mips64
.set noreorder
.set nobopt
.set noat

		.global test
test:		.ent test
		daddu 	$sp, $sp, -32
		sd	$ra, 24($sp)
		sd	$fp, 16($sp)
		daddu	$fp, $sp, 32

		dli	$a0, -1
# use a non-zero offset to make sure we don't get zero by accident
		li      $t0, 42
		csetoffset $c1, $c0, $t0
		ccleartag $c1, $c1
		ctoptr	$a0, $c1, $c0

		ld	$fp, 16($sp)
		ld	$ra, 24($sp)
		daddu	$sp, $sp, 32
		jr	$ra
		nop			# branch-delay slot
		.end	test
